//! points across the parametric space.
//!
//! ## Re-generate (auditor-reproducible)
//!
//! ```text
//! cc -O2 -Wall -I$(brew --prefix openssl@3)/include sm4_ccm_oracle.c \
//!    $(brew --prefix openssl@3)/lib/libcrypto.dylib -o sm4_ccm_oracle
//!
