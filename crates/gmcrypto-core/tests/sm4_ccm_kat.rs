#[test]
fn encrypt_matches_openssl_vectors() {
    for v in VECTORS {
        let got = mode_ccm::encrypt(&v.key, v.nonce, v.aad, v.plaintext, v.tag_len)
            .unwrap_or_else(|| panic!("encrypt returned None for scenario {:?}", v.name));
        assert_eq!(
            got, v.expected,
            "OpenSSL ct||tag divergence for scenario {:?}",
            v.name,
        );
    }
}

#[test]
fn decrypt_matches_openssl_vectors() {
    for v in VECTORS {
        let recovered = mode_ccm::decrypt(&v.key, v.nonce, v.aad, v.expected, v.tag_len);
        assert_eq!(
            recovered.as_deref(),
            Some(v.plaintext),
            "decrypt failed for scenario {:?}",
            v.name,
        );
    }
}

#[test]
fn tampered_tag_fails_for_all_vectors() {
    for v in VECTORS {
        let mut bad = v.expected.to_vec();
        let last = bad.len() - 1;
        bad[last] ^= 0x01;
        assert!(
            mode_ccm::decrypt(&v.key, v.nonce, v.aad, &bad, v.tag_len).is_none(),
            "tag-tamper not detected for scenario {:?}",
            v.name,
        );
    }
}

#[test]
fn tampered_aad_fails() {
    let v = &VECTORS[0]; // "baseline"
    let bad_aad = b"different aad value!";
    assert!(
        mode_ccm::decrypt(&v.key, v.nonce, bad_aad, v.expected, v.tag_len).is_none(),
        "AAD-tamper not detected for baseline",
    );
}
